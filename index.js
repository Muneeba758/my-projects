const answerElm = document.querySelectorAll(".answer");
const [questionElm, option_1, option_2, option_3, option_4] = document.querySelectorAll(
    "#question, .option_1, .option_2, .option_3, .option_4"
);
const submitBtn = document.querySelector("#submit");
let  currentQuiz=0;
let score=0;



const loadQuiz = () => {
    const { question, options } = mcqsData[currentQuiz];
    options.forEach((_, index) => {
        window[`option_${index + 1}`].classList.remove("correct", "wrong");
    });

    questionElm.innerText = question;

    options.forEach((curOption, index) => {
        window[`option_${index + 1}`].innerText = curOption;
        console.log("Current Quiz:", currentQuiz);
console.log(mcqsData[currentQuiz]);
    });
};
loadQuiz();

const getSelectedOption=()=>{
    let ans_index;
    answerElm.forEach((curOption, index)=>{
        if(curOption.checked){
            ans_index= index;
        }
    });
    return ans_index;
}
    const deselectedAnswers = () => {
    answerElm.forEach((curElm, index) => {
        curElm.checked = false;
        window[`option_${index + 1}`].classList.remove("correct", "wrong");
    });
};
 
submitBtn.addEventListener("click", () => {
    const selectedOptionIndex = getSelectedOption();

    if (selectedOptionIndex === null || selectedOptionIndex === undefined) {
    alert("Please select an option!");
    return;
}

    const correctIndex = mcqsData[currentQuiz].correct;

    
    answerElm.forEach((curElem, index) => {
        window[`option_${index + 1}`].classList.remove("correct", "wrong");
    });


    window[`option_${correctIndex + 1}`].classList.add("correct");

    if (selectedOptionIndex !== correctIndex) {
        window[`option_${selectedOptionIndex + 1}`].classList.add("wrong");
    } else {
        score++;
    }

    setTimeout(() => {
        currentQuiz++;

        if (currentQuiz < mcqsData.length) {
            deselectedAnswers();
            loadQuiz();
        } else {
            let totalQuestions = mcqsData.length;
            let percentage = (score / totalQuestions) * 100;

            let resultText = percentage >= 40
                ? "<span style='color:green;'>Pass ✅</span>"
                : "<span style='color:red;'>Fail ❌</span>";

            document.getElementById("result").innerHTML = `
    <h2>Your Score: ${score}/${totalQuestions}</h2>
    <h3>Percentage: ${percentage.toFixed(2)}%</h3>
    <h1>${resultText}</h1>
`;
        }
    }, 1000);
});